def executar_calculo():
    try:
        d1, d2 = input("Início (DD/MM/YYYY): "), input("Fim (DD/MM/YYYY): ")
        v_ini = float(input("Valor BRL: ").replace(',', '.'))

        # Busca CDI (12) e Dólar PTAX (1) de uma vez
        df = sgs.get({'cdi': 12, 'usd': 1},
                     start=datetime.strptime(d1, '%d/%m/%Y'),
                     end=datetime.strptime(d2, '%d/%m/%Y'))

        fator = (1 + df['cdi'][:-1] / 100).prod()
        v_fim = v_ini * fator

        c_ini, c_fim = df['usd'].iloc[0], df['usd'].iloc[-1]
        u_ini, u_fim = v_ini / c_ini, v_fim / c_fim

        print(f"\n--- RESULTADO ---\nFator CDI: {fator:.8f} (+{(fator-1)*100:.2f}%)")
        print(f"BRL: R$ {v_ini:,.2f} -> R$ {v_fim:,.2f}")
        print(f"USD: US$ {u_ini:,.2f} -> US$ {u_fim:,.2f} (Câmbio: {c_ini:.2f} -> {c_fim:.2f})")
        print(f"Ganho Real em USD: {(u_fim/u_ini - 1)*100:+.2f}%")
    except Exception as e: print(f"Erro: {e}")

executar_calculo()