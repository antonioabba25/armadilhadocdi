def carregar_caches():
    with open('usd_cache.json', 'r') as f:
        usd = json.load(f)
    with open('cdi_cache.json', 'r') as f:
        cdi = json.load(f)
    return usd, cdi

def obter_valor_com_fallback(dicionario, data_alvo):
    data_dt = datetime.strptime(data_alvo, "%Y-%m-%d")
    for _ in range(15):
        str_dt = data_dt.strftime("%Y-%m-%d")
        if str_dt in dicionario:
            return dicionario[str_dt], str_dt
        data_dt -= timedelta(days=1)
    return None, None

def calcular_resultado(data_ini_br, data_fim_br, valor_inicial_brl):
    usd_dict, cdi_dict = carregar_caches()
    d_ini = datetime.strptime(data_ini_br, "%d/%m/%Y").strftime("%Y-%m-%d")
    d_fim = datetime.strptime(data_fim_br, "%d/%m/%Y").strftime("%Y-%m-%d")

    # 1. CDI Acumulado (Regra exata validada)
    fator_acumulado = 1.0
    datas_ordenadas = sorted(cdi_dict.keys())
    for data in datas_ordenadas:
        if d_ini <= data < d_fim:
            taxa_diaria_perc = cdi_dict[data]
            fator_acumulado *= (1 + (taxa_diaria_perc / 100))

    # 2. Cotações USD
    cot_ini, _ = obter_valor_com_fallback(usd_dict, d_ini)
    cot_fim, _ = obter_valor_com_fallback(usd_dict, d_fim)

    if cot_ini is None or cot_fim is None:
        return "Erro: Dados de cotação insuficientes."

    # 3. Cálculos Financeiros
    v_final_brl = valor_inicial_brl * fator_acumulado
    v_ini_usd = valor_inicial_brl / cot_ini
    v_final_usd_ajustado = v_final_brl / cot_fim

    # Ganho real em USD (Comparação do investimento corrigido vs capital em dólar)
    rentabilidade_usd_real = (v_final_usd_ajustado / v_ini_usd - 1) * 100

    return {
        "periodo": f"{data_ini_br} a {data_fim_br}",
        "indice_correcao": fator_acumulado,
        "cdi_perc": (fator_acumulado - 1) * 100,
        "brl_ini": valor_inicial_brl,
        "brl_fim": v_final_brl,
        "usd_ini": v_ini_usd,
        "usd_fim_com_cdi": v_final_usd_ajustado,
        "cotacao_ini": cot_ini,
        "cotacao_fim": cot_fim,
        "rentabilidade_usd_real": rentabilidade_usd_real
    }

