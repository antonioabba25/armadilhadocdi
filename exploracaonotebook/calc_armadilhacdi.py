import matplotlib.pyplot as plt
from bcb import sgs
import pandas as pd
from datetime import datetime, timedelta
import json
import os

CACHE_FILE = "finance_cache.json"

def atualizar_e_carregar_dados():
    """Busca dados em blocos de 10 anos para contornar o limite da API e salva em cache."""
    hoje = datetime.now()
    df_cache = pd.DataFrame()

    if os.path.exists(CACHE_FILE):
        try:
            with open(CACHE_FILE, 'r') as f:
                cache = json.load(f)
                if cache:
                    df_cache = pd.DataFrame.from_dict(cache, orient='index')
                    df_cache.index = pd.to_datetime(df_cache.index)
        except Exception as e:
            print(f"Aviso: Cache corrompido, reiniciando... ({e})")
            os.remove(CACHE_FILE)

    if df_cache.empty:
        data_inicio_busca = datetime(1996, 1, 1)
    else:
        data_inicio_busca = df_cache.index.max() + timedelta(days=1)

    if data_inicio_busca < hoje - timedelta(days=1):
        print(f"Sincronizando dados desde {data_inicio_busca.strftime('%d/%m/%Y')}...")
        novos_dfs = []
        ponto_partida = data_inicio_busca

        while ponto_partida < hoje:
            ponto_final = min(ponto_partida + timedelta(days=365*9), hoje)
            try:
                chunk = sgs.get({'cdi': 12, 'usd': 1, 'ipca': 433},
                                start=ponto_partida, end=ponto_final)
                if not chunk.empty:
                    novos_dfs.append(chunk)
            except Exception as e:
                print(f"Aviso no bloco {ponto_partida.year}: {e}")
            ponto_partida = ponto_final + timedelta(days=1)

        if novos_dfs:
            df_novo = pd.concat(novos_dfs)
            df_completo = pd.concat([df_cache, df_novo]).sort_index()
            df_completo = df_completo[~df_completo.index.duplicated(keep='last')]

            df_para_cache = df_completo.copy()
            df_para_cache.index = df_para_cache.index.strftime('%Y-%m-%d')
            df_para_cache = df_para_cache.fillna(0)

            with open(CACHE_FILE, 'w') as f:
                json.dump(df_para_cache.to_dict(orient='index'), f)
            return df_completo

    return df_cache

def gerar_grafico_completo():
    try:
        df = atualizar_e_carregar_dados()
        if df.empty:
            print("Erro: Falha ao obter dados.")
            return

        d1_str = input("Início (DD/MM/YYYY): ")
        d2_str = input("Fim (DD/MM/YYYY): ")
        d1, d2 = pd.to_datetime(d1_str, dayfirst=True), pd.to_datetime(d2_str, dayfirst=True)

        df_view = df.loc[d1:d2].copy()
        if df_view.empty:
            print("Sem dados para o período selecionado.")
            return

        # Correção Crítica: Garantir que o USD não comece com zero ou NaN
        df_view['usd'] = df_view['usd'].replace(0, pd.NA).ffill().bfill()
        df_view['ipca'] = df_view['ipca'].fillna(0)
        df_view['cdi'] = df_view['cdi'].fillna(0)

        # Cálculos de evolução (Base 0% no primeiro dia válido)
        df_view['evolucao_cdi'] = (1 + df_view['cdi'] / 100).cumprod() - 1
        df_view['evolucao_ipca'] = (1 + df_view['ipca'] / 100).cumprod() - 1

        usd_inicial = df_view['usd'].iloc[0]
        df_view['evolucao_usd'] = (df_view['usd'] / usd_inicial) - 1

        # Ganho Real em USD: ((1 + CDI_acum) / (1 + USD_var)) - 1
        df_view['ganho_real_usd'] = ((1 + df_view['evolucao_cdi']) / (1 + df_view['evolucao_usd'])) - 1

        # Resample mensal para o gráfico
        plot_df = df_view[['evolucao_cdi', 'evolucao_ipca', 'evolucao_usd', 'ganho_real_usd']] * 100
        plot_df_mensal = plot_df.resample('ME').last()

        plt.figure(figsize=(12, 7))
        plt.plot(plot_df_mensal.index, plot_df_mensal['evolucao_cdi'], label='CDI (%)', color='blue', linewidth=2)
        plt.plot(plot_df_mensal.index, plot_df_mensal['evolucao_ipca'], label='IPCA (%)', color='orange', linestyle=':', linewidth=2)
        plt.plot(plot_df_mensal.index, plot_df_mensal['evolucao_usd'], label='USD (%)', color='green', alpha=0.6)
        plt.plot(plot_df_mensal.index, plot_df_mensal['ganho_real_usd'], label='Ganho Real USD (%)', color='red', linewidth=2)

        plt.title(f'Comparativo Histórico: {d1_str} a {d2_str}')
        plt.ylabel('Variação Acumulada (%)')
        plt.xlabel('Data')
        plt.legend(loc='upper left')
        plt.grid(True, alpha=0.3)
        plt.axhline(0, color='black', linewidth=1)
        plt.show()

    except Exception as e: print(f"Erro: {e}")

gerar_grafico_completo()

