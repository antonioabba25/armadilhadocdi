def buscar_historico_ptax(data_inicio="01-01-1996"):
    """
    Busca o histórico de cotações BRL/USD (venda) via API Olinda PTAX.
    Retorna um dicionário: { 'YYYY-MM-DD': cotação }
    """
    print(f"Iniciando coleta de dados USD desde {data_inicio}...")

    # Data final para a consulta (hoje)
    hoje = datetime.now().strftime("%m-%d-%Y")

    # URL da API Olinda (PTAX)
    url = (
        "https://olinda.bcb.gov.br/olinda/servico/PTAX/versao/v1/odata/"
        "CotacaoDolarPeriodo(dataInicial=@dataInicial,dataFinalCotacao=@dataFinalCotacao)?"
        f"@dataInicial='{data_inicio}'&@dataFinalCotacao='{hoje}'"
        "&$format=json&$select=cotacaoVenda,dataHoraCotacao"
    )

    try:
        # Realiza a requisição
        response = requests.get(url, timeout=45)
        response.raise_for_status()

        dados_brutos = response.json().get('value', [])

        # Processa os dados para o formato de dicionário
        usd_dict = {}
        for item in dados_brutos:
            # A data vem como '2023-10-27 13:05:45.123', pegamos apenas a parte da data
            data_iso = item['dataHoraCotacao'].split(' ')[0]
            usd_dict[data_iso] = float(item['cotacaoVenda'])

        print(f"Sucesso! {len(usd_dict)} cotações carregadas.")
        return usd_dict

    except Exception as e:
        print(f"Erro na coleta de dados: {e}")
        return {}

